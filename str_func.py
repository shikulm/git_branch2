def str_to_upper(str_inp: str) -> str:
    """
    Преобразовываем в заглавные буквы
    :param str_inp: входная строка
    :return: преобразованная в прописные буквы строка
    """
    return str_inp.upper()


def str_to_title(str_inp: str) -> str:
    """
    Делает заглавными первые буквы каждого слова в строке
    :param str_inp:  входная строка
    :return: преобразованная строка
    """
    return str_inp.title()




